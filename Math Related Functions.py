
x = 3.14
y = -6
z = 5

result1 = round(x)
# The 'round' function rounds the number to the nearest whole number

result2 = abs(y)
# The 'abs' function is used to find the absolute value of a number, the absolute value is the
# distance from zero, and is always positive

result3 = pow(z, 3)
# This would be 5 to the power of 3

result4 = max(x, y, z)
# This is used to find the maximum value in a list of numbers,

result5 = min(x, y, z)
# This is used to find the minimum value in a list of numbers

print(result1, result2, result3, result4, result5)
